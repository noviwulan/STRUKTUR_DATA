public class switchcase 
{
     public static void main(String[] args) 
    {

        int absen = 2;

        switch (absen) 
        {
            case 1:
                System.out.println("absen 1 adalah adam");
                break;
            case 2:
                System.out.println("absen 2 adalah bagus");
                break;
            case 3:
                System.out.println("absen 3 adalah chika");
                break;
            case 4:
                System.out.println("absen 4 adalah dika");
                break;
            default:
                System.out.println("Tidak ada pilihan");
        }
    }
}
